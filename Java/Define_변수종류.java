public class Define_변수종류 {

	public static void main(String[] args) { 
		
		boolean varBool = true;
		String varStr = "문자열";				// 여러 글자 (더블쿼터)
		char varChar = 'S';					// 한 글자 (싱글쿼터)
		
		int varInt = 0;							// 일반적인 정수
		long varLong = 1000000000; 		// 큰 정수
		
		double varDouble = 0.00001; 	// 큰 소수점 (일반적으로 소숫점 5자리 이상)
		float varFloat = 0.1f;				// float은 특수하게 f를 붙여준다.
		
	}

}
